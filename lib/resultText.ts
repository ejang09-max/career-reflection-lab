export function getSummary(assessmentCode: AssessmentCode, typeName: string) {
  if (assessmentCode === "values") {
    return `당신은 ${typeName} 가치를 중요하게 여기는 편입니다. 진로를 선택할 때 이 가치가 얼마나 충족되는지 살펴보면 더 자신 있는 의사결정에 도움이 될 수 있습니다.`;
  }
  if (assessmentCode === "multiple_intelligences") {
    return `당신은 ${typeName}이 강점으로 나타났습니다. 이 결과는 능력을 확정하는 의미가 아니라, 어떤 방식의 학습과 활동에서 더 잘 몰입할 수 있는지 살펴보는 참고자료입니다.`;
  }
  if (assessmentCode === "riasec") {
    return `당신은 ${typeName} 흥미가 상대적으로 두드러집니다. 특정 직업을 확정하는 결과가 아니라, 어떤 활동과 직업환경을 앞으로 탐색해보면 좋을지 살펴보는 참고자료입니다.`;
  }
  if (assessmentCode === "career_anchors") {
    return `당신은 ${typeName} 경력닻이 상대적으로 두드러집니다. 경력을 선택하고 유지할 때 어떤 가치와 욕구를 쉽게 포기하기 어려운지 성찰하는 데 도움이 될 수 있습니다.`;
  }
  if (assessmentCode === "career_adaptability") {
    return `당신은 ${typeName} 영역이 상대적으로 두드러집니다. 변화하는 진로환경 속에서 어떤 방식으로 준비하고 실행할 수 있는지 살펴보는 참고자료로 활용해 보세요.`;
  }
  return `당신은 ${typeName}이 강점 스킬로 나타났습니다. 관련 경험을 구체적인 행동과 결과로 정리하면 수업, 진로상담, 자기소개에서 활용하기 좋습니다.`;
}
